#1.& : if both bits are 1 then consider 1 , othewise 0
print( 13 & 6)

#2. | : if both bits are 0 then consider 0 , otherwise 1
print( 13 | 6)

#3. ^ : consider 1 for different bits, 0 for same bits
print( 13 ^ 6)

#4.~ :
print( ~ 10)

#5. << : 
print( 6 << 2)
print( 6 << 4)

#6. >> : 
print( 6 >> 1)
print( 6 >> 2)